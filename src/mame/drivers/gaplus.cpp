void gaplus_base_state::driver_init()
{
	uint8_t *rom = m_gfx1_region->base();
	for (int i = 0;i < 0x2000;i++)
		rom[i + 0x2000] = rom[i] >> 4;

	rom = m_gfx2_region->base() + 0x6000;
	for (int i = 0;i < 0x2000;i++)
		rom[i + 0x2000] = rom[i] << 4;
}


