ioport_constructor spectred_kbd_device::device_input_ports() const
{
	return INPUT_PORTS_NAME(spectred_kbd);
}

void spectred_kbd_device::device_add_mconfig(machine_config &config)
{
	mcs48_cpu_device &mcu(I8035(config, "mcu", 6'000'000)); // unknown clock
	mcu.set_addrmap(AS_PROGRAM, &spectred_kbd_device::prog_map);
	mcu.p1_out_cb().set(FUNC(spectred_kbd_device::p1_w));
	mcu.p2_out_cb().set(FUNC(spectred_kbd_device::p2_w));
	mcu.t1_in_cb().set(FUNC(spectred_kbd_device::key_r));
}

