void mm2_keyboard_device::device_start()
{
}

void mm2_keyboard_device::device_reset()
{
	set_data_frame(1, 8, PARITY_EVEN, STOP_BITS_1);
	set_rcv_rate(9600);
	set_tra_rate(9600);
	receive_register_reset();
	transmit_register_reset();

	clear_fifo();
	reset_key_state();
	start_processing(attotime::from_hz(600));
}
