for alg in range(8):
    start = alg * 32
    wacc_pcs = []
    mix_pcs = []  # WXY WSP updates MIXL/MIXR
    
    for pc in range(32):
        inst = decode_instruction(aram[start + pc])
        if inst.wacc:
            wacc_pcs.append(pc)
        if inst.wxy and inst.wsp:
            mix_pcs.append(pc)
    
    used_by = [s for s, cfg in slots.items() if cfg['alg'] == alg and not cfg['idle']]
    idle_by = [s for s, cfg in slots.items() if cfg['alg'] == alg and cfg['idle']]
    
    print(f"\nAlgorithm {alg}:")
    print(f"  Used by active slots: {used_by if used_by else 'none'}")
    print(f"  Would be used by idle slots: {idle_by if idle_by else 'none'}")
    print(f"  WACC instructions at PC: {wacc_pcs if wacc_pcs else 'none'}")
    print(f"  MIX update (WXY+WSP) at PC: {mix_pcs if mix_pcs else 'none'}")

print("\n" + "=" * 70)
print("Summary: Which slots output audio?")
print("=" * 70)

for slot, cfg in slots.items():
    alg = cfg['alg']
    start = alg * 32
    
    has_wacc = any(decode_instruction(aram[start + pc]).wacc for pc in range(32))
    has_mix = any(decode_instruction(aram[start + pc]).wxy and 
                  decode_instruction(aram[start + pc]).wsp for pc in range(32))
    
    status = "IDLE" if cfg['idle'] else "ACTIVE"
    output = "YES" if has_wacc else "NO"
    mix = "YES" if has_mix else "NO"
    
    if cfg['idle']:
        print(f"Slot {slot:2d}: {status} (ALG {alg}) - would have WACC={output}, MIX={mix} if active")
    else:
        print(f"Slot {slot:2d}: {status} (ALG {alg}) - WACC={output}, MIX={mix}")
